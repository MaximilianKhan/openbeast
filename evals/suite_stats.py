#!/usr/bin/env python3
"""
Suite statistics generator — the single source of truth for eval-suite counts.

Docs used to hand-write counts (313 / 323 / 223 effective units all appeared
at various times); this computes them from evals/tasks/*.json so they can't
drift. Cite THESE numbers, or better, paste the --markdown output.

Usage:
  python3 evals/suite_stats.py               # human-readable summary
  python3 evals/suite_stats.py --markdown    # docs-ready markdown block
  python3 evals/suite_stats.py --json        # machine-readable
  python3 evals/suite_stats.py --check       # verify README/evals docs cite
                                             # the current headline numbers
                                             # (exit 1 on drift) — used by tests
"""

import argparse
import glob
import json
import os
import re
import sys
from collections import Counter

EVALS_DIR = os.path.dirname(os.path.abspath(__file__))
REPO_ROOT = os.path.dirname(EVALS_DIR)
TASKS_GLOB = os.path.join(EVALS_DIR, "tasks", "*.json")

# Docs that must cite the current headline numbers (checked by --check).
CHECKED_DOCS = ["README.md", os.path.join("evals", "README.md")]


def collect() -> dict:
    """Compute all suite statistics from the task specs on disk."""
    base_tasks = 0
    effective_units = 0
    varianted_tasks = 0
    diff_base = Counter()
    diff_units = Counter()
    lang_units = Counter()
    categories = Counter()

    for path in sorted(glob.glob(TASKS_GLOB)):
        with open(path) as f:
            spec = json.load(f)
        base_tasks += 1
        diff = spec.get("difficulty", "medium")
        diff_base[diff] += 1
        categories[spec.get("category", "(uncategorized)")] += 1

        variants = spec.get("variants")
        if variants:
            varianted_tasks += 1
            effective_units += len(variants)
            diff_units[diff] += len(variants)
            for v in variants:
                lang_units[v.get("language", "python")] += 1
        else:
            effective_units += 1
            diff_units[diff] += 1
            lang_units["python"] += 1

    suite_version = "unknown"
    ver_path = os.path.join(EVALS_DIR, "SUITE_VERSION")
    if os.path.exists(ver_path):
        with open(ver_path) as f:
            suite_version = f.read().strip() or "unknown"

    return {
        "suite_version": suite_version,
        "base_tasks": base_tasks,
        "effective_units": effective_units,
        "varianted_tasks": varianted_tasks,
        "difficulty_base": dict(diff_base),
        "difficulty_units": dict(diff_units),
        "language_units": dict(lang_units),
        "categories": dict(categories.most_common()),
    }


def render_text(s: dict) -> str:
    lines = [
        f"Suite {s['suite_version']}: {s['base_tasks']} base tasks / "
        f"{s['effective_units']} effective units "
        f"({s['varianted_tasks']} tasks with language variants)",
        "",
        "Difficulty (base tasks / effective units):",
    ]
    for d in ("easy", "medium", "hard"):
        lines.append(
            f"  {d:<7} {s['difficulty_base'].get(d, 0):>4} / "
            f"{s['difficulty_units'].get(d, 0):>4}"
        )
    lines.append("")
    lines.append("Language (effective units):")
    for lang, n in sorted(s["language_units"].items(), key=lambda kv: -kv[1]):
        lines.append(f"  {lang:<7} {n:>4}")
    lines.append("")
    lines.append(f"Categories ({len(s['categories'])}):")
    for cat, n in s["categories"].items():
        lines.append(f"  {n:>4}  {cat}")
    return "\n".join(lines)


def render_markdown(s: dict) -> str:
    langs = sorted(s["language_units"].items(), key=lambda kv: -kv[1])
    lines = [
        "<!-- generated by evals/suite_stats.py — do not hand-edit counts -->",
        f"**Suite {s['suite_version']}**: {s['base_tasks']} base tasks / "
        f"{s['effective_units']} effective units, {s['varianted_tasks']} tasks "
        f"with multi-language variants across {len(langs)} languages.",
        "",
        "| Difficulty | Base tasks | Effective units |",
        "|---|---:|---:|",
    ]
    for d in ("easy", "medium", "hard"):
        lines.append(
            f"| {d} | {s['difficulty_base'].get(d, 0)} | "
            f"{s['difficulty_units'].get(d, 0)} |"
        )
    lines += [
        "",
        "| Language | Effective units |",
        "|---|---:|",
    ]
    for lang, n in langs:
        lines.append(f"| {lang} | {n} |")
    return "\n".join(lines)


def check_docs(s: dict) -> int:
    """Verify the headline numbers appear in the checked docs. Fails (1) if a
    doc cites a stale base-task or effective-unit count in its headline claim
    ("<N> base tasks" / "<N> effective units" / "<N> units")."""
    base, units = s["base_tasks"], s["effective_units"]
    ok = True
    for rel in CHECKED_DOCS:
        path = os.path.join(REPO_ROOT, rel)
        if not os.path.exists(path):
            print(f"  SKIP {rel} (missing)")
            continue
        with open(path) as f:
            text = f.read()
        # Any "<N> base tasks" claim must cite the current count.
        stale = set()
        for m in re.finditer(r"(\d+)\s+base\s+tasks", text):
            if int(m.group(1)) != base:
                stale.add(f"{m.group(1)} base tasks (now {base})")
        for m in re.finditer(r"(\d+)\s+effective\s+units", text):
            if int(m.group(1)) != units:
                stale.add(f"{m.group(1)} effective units (now {units})")
        if stale:
            ok = False
            print(f"  DRIFT in {rel}:")
            for msg in sorted(stale):
                print(f"    - cites {msg}")
        else:
            print(f"  OK   {rel}")
    return 0 if ok else 1


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--markdown", action="store_true")
    ap.add_argument("--json", action="store_true")
    ap.add_argument("--check", action="store_true")
    args = ap.parse_args()

    s = collect()
    if args.check:
        print(f"suite {s['suite_version']}: {s['base_tasks']} base / "
              f"{s['effective_units']} units — checking docs:")
        return check_docs(s)
    if args.json:
        print(json.dumps(s, indent=2))
    elif args.markdown:
        print(render_markdown(s))
    else:
        print(render_text(s))
    return 0


if __name__ == "__main__":
    sys.exit(main())
